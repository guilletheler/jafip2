package com.gt.ifepson.comandos;

/**
 *
 * @author guillermot
 */
public class ValorExtension {
    int valor;
    
    String nombre;
    
    TipoExtension tipoExtension;

    public ValorExtension(TipoExtension tipoExtension, String nombre, int valor) {
        this.tipoExtension = tipoExtension;
        this.valor = valor;
        this.nombre = nombre;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public TipoExtension getTipoExtension() {
        return tipoExtension;
    }

    public void setTipoExtension(TipoExtension tipoExtension) {
        this.tipoExtension = tipoExtension;
    }
    
}
